#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*copy;
	int		i;
	int		j;

	i = 0;
	while (src[i] != '\0')
		i++;
	copy = (char *)malloc(sizeof(char) * (i + 1));
	j = 0;
	if (copy == NULL)
		return (0);
	while (src[j] != '\0')
	{
		copy[j] = src[j];
		j++;
	}
	copy[j] = '\0';
	return (copy);
}

